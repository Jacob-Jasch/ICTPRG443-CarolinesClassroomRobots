package com.company;

public class ClassRoomLayout
{

    /**
     * random variables
     */
    String X;
    String Y;
    String Name;
    String Background;


    /**
     * some variables to define what the classroomlayout is
     * used in other class's
     * @param x
     * @param y
     * @param name
     * @param background
     */
    public ClassRoomLayout(String x, String y, String name, String background)
    {
        X = x;
        Y = y;
        Name = name;
        Background = background;
    }
}